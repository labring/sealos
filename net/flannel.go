package net

type Flannel struct {
	metadata MetaData
}

func (f Flannel) Manifests(template string) string {
	if template == "" {
		template = f.Template()
	}
	if f.metadata.Interface == "" {
		f.metadata.Interface = defaultInterface
	}
	if f.metadata.CIDR == "" {
		f.metadata.CIDR = defaultCIDR
	}

	if f.metadata.CniRepo == "" || f.metadata.CniRepo == "k8s.gcr.io" {
		f.metadata.CniRepo = "quay.io.azk8s.cn/coreos"
	}

	return render(f.metadata, template)
}

func (Flannel) Template() string {
	return FlannelManifests
}

